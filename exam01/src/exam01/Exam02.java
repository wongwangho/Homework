package exam01;

import java.util.Scanner;

public class Exam02 {

	public static void main(String[] args) {

		//반복문을 사용하여 1부터 5까지의 합을 구한다 
		//num 변수에 합을 저장하고 출력한다
		
		
		int num = 0;
		for(int i = 1; i <= 5; i++) {
			
			num += i;
			
		}
		
		System.out.println(num);
		
		
	}

}
